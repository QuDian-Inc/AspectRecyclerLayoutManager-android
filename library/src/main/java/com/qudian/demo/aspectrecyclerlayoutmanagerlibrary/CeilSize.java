package com.qudian.demo.aspectrecyclerlayoutmanagerlibrary;

public class CeilSize {
    int mWidth;
    int mHeight;

    public CeilSize(int width, int height) {
        mWidth = width;
        mHeight = height;
    }

    public int getWidth() {
        return mWidth;
    }

    public int getHeight() {
        return mHeight;
    }
}
